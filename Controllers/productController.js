// Create order
const createOrder = async (req, res) => {
  try {
    const { amount, currency } = req.body;

    if (!amount || !currency) {
      return res.status(400).json({ error: "Amount and currency are required" });
    }

    const options = {
      amount: amount * 100, // Convert to smallest currency unit (paise)
      currency,
      receipt: `order_rcptid_${Date.now()}`, // Fixed template literal syntax
    };

    const order = await razorpay.orders.create(options);
    res.json(order);
  } catch (error) {
    console.error("Error creating Razorpay order:", error);
    res.status(500).json({ error: "Internal Server Error" });
  }
};


const verifyPayment = async (req, res) => {
  try {
    console.log("Received Request Body:", req.body);

    const { razorpay_order_id, razorpay_payment_id, razorpay_signature, cart, user } = req.body;

    // 🚨 Validate request body
    if (!razorpay_order_id || !razorpay_payment_id || !razorpay_signature) {
      return res.status(400).json({ error: "Missing payment details" });
    }
    if (!user || !user._id) {
      return res.status(400).json({ error: "Invalid user data" });
    }
    if (!Array.isArray(cart) || cart.length === 0) {
      return res.status(400).json({ error: "Cart is empty or invalid" });
    }

    // 🔐 Verify Razorpay signature
    const body = `${razorpay_order_id}|${razorpay_payment_id}`;
    const expectedSignature = crypto
      .createHmac("sha256", process.env.RAZORPAY_KEY_SECRET)
      .update(body)
      .digest("hex");

    if (expectedSignature !== razorpay_signature) {
      return res.status(400).json({ error: "Invalid signature" });
    }

    // 🛒 Validate products before saving order
    const formattedCart = cart.map((item) => ({
      name: item.name,
      price: item.price,
      quantity: item.quantity,
      productId: item._id || null, // Ensure productId is present
    }));

    // 📝 Save order to the database
    const newOrder = new Order({
      user: user._id,
      products: formattedCart,
      paymentInfo: {
        orderId: razorpay_order_id,
        paymentId: razorpay_payment_id,
        signature: razorpay_signature,
      },
      amount: formattedCart.reduce((total, item) => total + item.price * item.quantity, 0),
      //status: "Paid",
    });

    console.log("Saving Order for User ID:", user._id);
    await newOrder.save();

    res.json({ success: true, message: "Payment verified and order saved!" });
  } catch (error) {
    console.error("Payment verification error:", error);
    res.status(500).json({ error: "Internal Server Error" });
  }
};







